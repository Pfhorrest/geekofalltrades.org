	<h2>The Oumen Saga</h2>
	
	<ol>
		<li>
			In the Oumen saga,
			a utopian future Earth turns out to have been orchestrated by the immortals who inspired all ancient mythologies, one of whom becomes its only hope after the collapse of civilization.
			<ol>
				<li>
					In <a href="1">the Pantheon series</a>,
					immortal humans on prehistoric Earth inspire all ancient mythologies while trying to escape back to space, from where they guide mortal humanity into a utopian future.
					<ol>
						<li>
							In <a href="1/1">Pantheon Genesis</a>,
							a family of immortals created by Keius Meij crash land on prehistoric Earth, then try to rebuild their way back to space, until rising sea levels drown their nascent civilization.
						</li>
						<li>
							In <a href="1/2">Pantheon Exodus</a>,
							the immortals are divided in a schism that kills one of them, Metis, and they wander the ancient world until eventually regrouping for a successful second attempt to escape to space.
						</li>
						<li>
							In <a href="1/3">Pantheon Synthesis</a>,
							the now-spacefaring immortals begin to secretly intervene in the affairs of the advancing mortal civilization, subtly guiding it through terrible wars for the protection of all mankind.
						</li>
					</ol>
				</li>
				<li>
					In <a href="2">the Foundation series</a>,
					the immortals begin to intervene in mortal human affairs, ushering in a bright utopian future, until the whole thing comes crashing down upon Disastrous first contact with alien life.
					<ol>
						<li>
							In <a href="2/1">Shadow of the Foundation</a>,
							the secretive Foundation begins to intervene more actively in the affairs of human civilization, covertly averting multiple existential crises while subtly ushering in a bright utopian future.
						</li>
						<li>
							In <a href="2/2">Veil of the Foundation</a>,
							an expedition called the Einherjar, working for the Foundation, are sent the long way around to explore what lies on the other end of the sealed-off wormhole from Sol.
						</li>
						<li>
							In <a href="2/3">Light of the Foundation</a>,
							humanity rallies behind the Foundation against the aliens who lie beyond the wormhole, only to be drawn into an already-ongoing galactic war, leading to the destruction of Earth.
						</li>
					</ol>
				</li>
				<li>
					In <a href="3">the Resurgent series</a>,
					Metis is the sole surviving immortal on post-apocalyptic Earth, and gradually guides the remnants of mortal human civilization back to their previous heights.
					<ol>
						<li>
							In <a href="3/1">Columbia Resurgent</a>,
							Metis is uncovered centuries later as the sole surviving immortal on Earth, and slowly leads a reunification of the continent of Columbia where she awoke.
						</li>
						<li>
							In <a href="3/2">Terra Resurgent</a>,
							the reunited Columbian civilization encounters two rival civilizations from across the seas, and Metis uses her influence as a mythological figure to broker peace between them all.
						</li>
						<li>
							In <a href="3/3">Sol Resurgent</a>,
							the reunified Earth rebuilds back to space with the help of automatons who survived the war, only to discover that evil immortals have since conquered the rest of the galaxy.
						</li>
					</ol>
				</li>
			</ol>
		</li>
	</ol>

</section>
