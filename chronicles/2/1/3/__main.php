<section>
	
	<h2>Pantheon Synthesis</h2>
	
	<ol>
		<li>
			In Pantheon Synthesis,
			the now-spacefaring immortals begin to secretly intervene in the affairs of the advancing mortal civilization, subtly guiding it through terrible wars for the protection of all mankind.
			<ol>
				<li>
					In <a href="1">Part 1</a>,
					as mortal human nations become global superpowers with nuclear weaponry, the immortals debate whether to begin interfering with mortal affairs.
					<ol>
						<li>
							In <a href="1/1">Episode 1</a>,
							the Pantheon builds out their presence in space as humanity builds up to its industrial age, and the Pantheon begin to contemplate intervention again.
						</li>
						<li>
							In <a href="1/2">Episode 2</a>,
							mortal humanity develop astronomical technology advanced enough to detect the Pantheon in space, pouring fuel onto the interventionist-isolationist debate.
						</li>
						<li>
							In <a href="1/3">Episode 3</a>,
							a World War breaks out among mortal human nations, and the advent of nuclear weapons forces the Pantheon to adopt a slightly more interventionist stance than they have been.
						</li>
					</ol>
				</li>
				<li>
					In <a href="2">Part 2</a>,
					the immortals begin to actively interfere with mortal human affairs as the world moves to a century-long state of impending nuclear armageddon.
					<ol>
						<li>
							In <a href="2/1">Episode 1</a>,
							as the mortal world becomes polarized between nationalism and communism, each armed with nuclear weapons, the Pantheon adopt the facade of "the Foundation" to covertly intervene.
						</li>
						<li>
							In <a href="2/2">Episode 2</a>,
							Foundation spaceships and pilots spur rumors of extraterrestrials, and a crash in the Columbian southwest almost exposes the entire scheme.
						</li>
						<li>
							In <a href="2/3">Episode 3</a>,
							mortal space technology advances to the point that the Pantheon must once again relocate before they are discovered, this time withdrawing to the Jovian system.
						</li>
					</ol>
				</li>
				<li>
					In <a href="3">Part 3</a>,
					the immortals must move further from Earth to escape the notice of advancing mortal technology, while debating if they should continue intervening even without imminent nuclear threats.
					<ol>
						<li>
							In <a href="3/1">Episode 1</a>,
							only a few Foundation agents remain on Earth while the rest of the immortals relocate, and as they watch mortal social conditions deteriorate, a few of them decide to act alone.
						</li>
						<li>
							In <a href="3/2">Episode 2</a>,
							rogue Foundation agents covertly recruit well-educated but politically skeptical people from the nationalist world powers to begin building a new society in the Pacific Ocean.
						</li>
						<li>
							In <a href="3/3">Episode 3</a>,
							the growth of the Pacific Islands International Coalition attracts the attention of the rest of the Foundation, to the detriment of the rogue agents who started it.
						</li>
					</ol>
				</li>
			</ol>
		</li>
	</ol>

</section>
