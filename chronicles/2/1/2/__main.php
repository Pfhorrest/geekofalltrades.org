	<h2>Pantheon Exodus</h2>
	
	<ol>
		<li>
			In Pantheon Exodus,
			the immortals are divided in a schism, and they wander the ancient world until eventually regrouping for a successful second attempt to escape to space.
			<ol>
				<li>
					In <a href="1">Part 1</a>,
					a family of immortals created by Keius Meij crash land on prehistoric Earth, then try to rebuild their way back to space, until rising sea levels drown their nascent civilization.
					<ol>
						<li>
							In <a href="1/1">Episode 1</a>,
							after a time lost at sea after the flooding of Auei, the immortals settle in the Indus River Valley, then split into two factions in the absence of their old leaders.
						</li>
						<li>
							In <a href="1/2">Episode 2</a>,
							Xio, the youngest son of Kron, survives his attempted suicide, only to try it again and again, eventually settling in China where he tries instead to atone for his sins.
						</li>
						<li>
							In <a href="1/3">Episode 3</a>,
							the divided immortals migrate to the Steppe seeking the guidance of the first immortal; Xio wanders that way too and ends up part of their new ruling council.
						</li>
					</ol>
				</li>
				<li>
					In <a href="2">Part 2</a>,
					the scattered immortals regroup and move westward in time to withness the dawn of written history, which diverges from the history of our own records.
					<ol>
						<li>
							In <a href="2/1">Episode 1</a>,
							as the people of the Steppe spread, the they encounter the descendants of the immortals' old regents in north Africa, who become singularly influential in shaping world history.
						</li>
						<li>
							In <a href="2/2">Episode 2</a>,
							descendants of the Steppe peoples build their own empire in southern Europe, which unites with the north African empire, all of whom worship the immortals as gods.
						</li>
						<li>
							In <a href="2/3">Episode 3</a>,
							the immortals move to northern Europe to try to stay out of influencing mortal affairs, where Kron begins to reassert himself, this time more passive-aggressively.
						</li>
					</ol>
				</li>
				<li>
					In <a href="3">Part 3</a>,
					the immortals cross the Atlantic ocean to the remote Andes mountains, where they found another civilization and succeed at last in building back to space.
					<ol>
						<li>
							In <a href="3/1">Episode 1</a>,
							encroaching civilization across Eurasia prompts the immortals to seek a new more distant location to restart their old plan to build back to space: America.
						</li>
						<li>
							In <a href="3/2">Episode 2</a>,
							the immortals make the perilous journey by ship to America, where they encounter a civilization descended from the banished faction of their antedeluvian civilization.
						</li>
						<li>
							In <a href="3/3">Episode 3</a>,
							the immortals finally build their way back to space; Kron attempts to betray them again, only for their mortal civilization to deal with him for them.
						</li>
					</ol>
				</li>
			</ol>
		</li>
	</ol>

