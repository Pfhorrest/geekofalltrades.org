	<h2>Terra Resurgent</h2>
	
	<ol>
		<li>
			In Terra Resurgent,
			the reunited Columbian civilization encounters two rival civilizations from across the seas, and Metis uses her influence as a mythological figure to broker peace between them all.
			<ol>
				<li>
					In <a href="1">Part 1</a>,
					Metis and a Columbian expedition searching for Xio at the base of Starbridge America encounter two more powerful groups of survivors from across the seas, Pacifica and Alexandria.
					<ol>
						<li>
							In <a href="1/1">Episode 1</a>,
							centuries after the reunification of Columbia, Metis leads an expedition following the fallen ancient Starbridge America toward its base in the Andes, where they hope to find Xio buried.
						</li>
						<li>
							In <a href="1/2">Episode 2</a>,
							Metis and her expedition encounter a settlement from the Empire of Pacifica on the western shores of America, only to be expelled from their territory.
						</li>
						<li>
							In <a href="1/3">Episode 3</a>,
							Metis and her expedition circle around to the east of America where they encounter and tentatively befriend forces from Alexandria, across the Atlantic sea.
						</li>
					</ol>
				</li>
				<li>
					In <a href="2">Part 2</a>,
					Metis travels to Alexandria, where she reveals herself as a goddess from the religion they still practice, and uses that position to help negotiate a truce between them and Columbia.
					<ol>
						<li>
							In <a href="2/1">Episode 1</a>,
							as the expedition escort an Alexandrian representative to Columbia to begin peace talks, Metis travels with the Alexandrians back to their homeland to learn more about them.
						</li>
						<li>
							In <a href="2/2">Episode 2</a>,
							Metis studies in Alexandria's famous library to relearn the history of their syncretic religion in which she herself appears to be a central figure.
						</li>
						<li>
							In <a href="2/3">Episode 3</a>,
							Metis reveals herself to the Alexandrians as the goddess from their religion, and travels with them back to Columbia where a mutual non-aggression treaty is signed.
						</li>
					</ol>
				</li>
				<li>
					In <a href="3">Part 3</a>,
					Metis and an expedition of Columbian representatives negotiate a peace with Pacifica, and begin the process of brokering world peace between them and Alexandria.
					<ol>
						<li>
							In <a href="3/1">Episode 1</a>,
							Metis and a better-armed Columbian expedition revisit the Pacific States of America, where the Columbians begin political negotiations and Metis reveals her godhood to them.
						</li>
						<li>
							In <a href="3/2">Episode 2</a>,
							Metis and the Columbian expedition visit Pacifica itself, where the Columbians negotiate another non-aggression treaty while Metis gives an interview on her history.
						</li>
						<li>
							In <a href="3/3">Episode 3</a>,
							as Columbia hosts a world peace seminar with Pacifica and Alexandria, Metis excavates the base of Starbridge America and finds Xio's buried ship containing powerful Foundation artifacts.
						</li>
					</ol>
				</li>
			</ol>
		</li>
	</ol>

