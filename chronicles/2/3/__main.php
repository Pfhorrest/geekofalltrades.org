	<h2>The Resurgent Series</h2>
	
	<ol>
		<li>
			In the Resurgent series,
			the sole surviving immortal on post-apocalyptic Earth gradually guides the remnants of mortal human civilization back to their previous heights.
			<ol>
				<li>
					In <a href="1">Columbia Resurgent</a>,
					Metis is uncovered centuries later as the sole surviving immortal on Earth, and slowly leads a reunification of the continent of Columbia where she awoke.
					<ol>
						<li>
							In <a href="1/1">Part 1</a>,
							Metis awakens after thousands of years in a post-apocalyptic future Earth, befriends the primitive survivors of that time, and rediscovers her immortality.
						</li>
						<li>
							In <a href="1/2">Part 2</a>,
							while working with friendly powers to investigate her forgotten origins, Metis is drawn into and exacerbates a conflict between them and a domineering neighbor state.
						</li>
						<li>
							In <a href="1/3">Part 3</a>,
							Metis helps lead a coordinated war effort against the Redstone Empire, gradually reuniting the disparate parts of the former United States of Columbia to defeat them.
						</li>
					</ol>
				</li>
				<li>
					In <a href="2">Terra Resurgent</a>,
					the reunited Columbian civilization encounters two rival civilizations from across the seas, and Metis uses her influence as a mythological figure to broker peace between them all.
					<ol>
						<li>
							In <a href="2/1">Part 1</a>,
							Metis and a Columbian expedition searching for Xio at the base of Starbridge America encounter two more powerful groups of survivors from across the seas, Pacifica and Alexandria.
						</li>
						<li>
							In <a href="2/2">Part 2</a>,
							Metis travels to Alexandria, where she reveals herself as a goddess from the religion they still practice, and uses that position to help negotiate a truce between them and Columbia.
						</li>
						<li>
							In <a href="2/3">Part 3</a>,
							Metis and an expedition of Columbian representatives negotiate a peace with Pacifica, and begin the process of brokering world peace between them and Alexandria.
						</li>
					</ol>
				</li>
				<li>
					In <a href="3">Sol Resurgent</a>,
					the reunified Earth rebuilds back to space with the help of automatons who survived the war, only to discover that evil immortals have since conquered the rest of the galaxy.
					<ol>
						<li>
							In <a href="3/1">Part 1</a>,
							human civilization rebuilds to the space age, teetering on the brink of nuclear war, until Metis reestablishes contact with the ancient automatons who have been terraforming Venus.
						</li>
						<li>
							In <a href="3/2">Part 2</a>,
							humanity undergoes a difficult socioeconomic transition back into an automated post-scarcity economy, as they return to space and begin to resettle the solar system.
						</li>
						<li>
							In <a href="3/3">Part 3</a>,
							Metis explores the rest of the galaxy beyond the wormhole, finding it to be ruled by her uncle Kron and his brothers, to whom she is forced to pledge subservience.
						</li>
					</ol>
				</li>
			</ol>
		</li>
	</ol>

