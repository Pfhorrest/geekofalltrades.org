	<h2>Shadow of the Foundation</h2>
	
	<ol>
		<li>
			In Shadow of the Foundation,
			the now-spacefaring immortals begin to secretly intervene in the affairs of the advancing mortal civilization, subtly guiding it through terrible wars toward a bright utopian future.
			<ol>
				<li>
					In <a href="1">Part 1</a>,
					the accidental discovery of the "food of the gods" starts a civil war in Xiania, and attempts to resolve that situation seem to only make it worse.
					<ol>
						<li>
							In <a href="1/1">Episode 1</a>,
							the accidental mortal discovery of the immortality-inducing "food of the gods" forces Xio to personally intervene in the subsequent civil war in Xiania.
						</li>
						<li>
							In <a href="1/2">Episode 2</a>,
							Xio's intervention in the Xianian civil war nearly leads to a world-ending catastrophe as a gravitic superweapon is prepared to fight a robot army gone rogue.
						</li>
						<li>
							In <a href="1/3">Episode 3</a>,
							the renegade robot army rolling over Asia and the Columbian swarm of hunter-killed drones are both subdued by a friendly robot force from the new nation of Pacifica.
						</li>
					</ol>
				</li>
				<li>
					In <a href="2">Part 2</a>,
					the discovery of Kron's body buried in the Andes accidentally sets off a "zombie apocalypse", which Kron rides out while the rest of the world begins to panic.
					<ol>
						<li>
							In <a href="2/1">Episode 1</a>,
							archaologists unearth the body of Kron in the Andes, and accidentally set off a "zombie apocalype" when his blood is unwittingly mixed with that of mortals.
						</li>
						<li>
							In <a href="2/2">Episode 2</a>,
							a regenerated but amnesiac Kron fights to survive the American "zombie apocalypse" while slowly regaining his memories of his true identity.
						</li>
						<li>
							In <a href="2/3">Episode 3</a>,
							the "food of the gods" accidentally discovered in Xiania proves to be the cure needed to end the American "zombie apocalypse", when delivered by the Pacifican automatons.
						</li>
					</ol>
				</li>
				<li>
					In <a href="3">Part 3</a>,
					the immortals embrace their role as hidden guides of the burgeoning global civilization, and subtly influence the emergence of a social and technological utopia.
					<ol>
						<li>
							In <a href="3/1">Episode 1</a>,
							Kron reluctantly joins the rest of the immortals in the Foundation, and under their secret guidance the war-torn world is rebuilt into the Internation.
						</li>
						<li>
							In <a href="3/2">Episode 2</a>,
							the Internation puts economic pressure on the oppressive world superpowers of Auei and Columbia, until they collapse from within and their peoples join the Internation.
						</li>
						<li>
							In <a href="3/3">Episode 3</a>,
							Africa is liberated from its European colonists to join with the Internation as well, eventually pressuring even Europe itself to join the rest of the unified world.
						</li>
					</ol>
				</li>
			</ol>
		</li>
	</ol>

