					<p><strong>Begin with <a href="2/2/2/2/2">Season 1, Episode 1.</a></strong></p>
					<p><i>The Chronicles of Quelouva</i> is an epic series of stories spanning tens of thousands of years of fictional history, past and future; "epic" in the original sense of that word meaning many small independent stories that connect together into a much larger story.
					"Quelouva" is the name of the setting, our galaxy the Milky Way, in the language of an alien race called <a href="1">the Ehrban, whose saga</a> composes one third of the stories, chronicling the rise and fall and eventual resurgence of their ancient galactic empire.
					The entire series takes place in a timeline alternate to our own, though this is only relevant near the contemporary parts of <a href="2">the Oumen saga</a>, which composes another third of the stories, chronicling the mythological prehistory of human civilization through to the post-apocalyptic future.
					The remaining third of the stories constitute <a href="3">the Sphidi saga</a>, chronicling the events of a virtual world that forgets that it is virtual, takes on a life of its own, and eventually heralds the end of galactic history.</p>
					<p>The settings of each saga are thus widely different in scope. On one end of the spectrum, the Sphidi saga takes place almost entirely within the simulated world called Virtuality, exploring "inner space" themes. The Oumen saga meanwhile takes place across the entirety of the Earth, its solar system, and even briefly ventures beyond. And at the far end of the spectrum, the Ehrban saga ranges far and wide across the entire galaxy of Quelouva, with bookends reaching out to other galaxies still, exploring "outer space" themes. By the end of the series the scopes converge, however, as events within Virtuality reshape not only the whole world but the entire galaxy, and one human from Earth becomes the only hope for all of Quelouva's salvation.</p>

					<p>These three sagas are not chronologically sequential, but rather overlap significantly. Each is divided into three parts, alternately spanning time periods of either around a single lifetime, or many thousands of years.
					These periods alternate between plateaus of high times, for the parts that span a single lifetime, and long periods of rebuilding, for the parts that span many thousands of years.</p>
					<ul>
						<li>
							The first part of the Ehrban saga spans a single lifetime, at the height of Ehrban civilization, and is not contemporary with any other stories.
						</li>
						<li>
							The second part of the Ehrban saga takes places immediately after the first part and spans around twelve thousand years, slowly rebuilding from a catastrophe. It runs contemporaneously with but separately from the first part of the Oumen saga, in which humanity slowly build up from nothing.
						</li>
						<li>
							The third part of the Ehrban saga again spans a single lifetime, back at a new height of Ehrban civilization. Contemporary to that run the second part of the Oumen saga, as humanity reaches the height of its civilization, and the first part of the Sphidi saga, where the new virtual world is at its height. At the start of this period the events of the Oumen saga give rise to the start of the Sphidi saga, and the during the end of this period the end of the Ehrban saga finally reconnects with the events of the Oumen saga.
						</li>
						<li>
							The third part of the Oumen saga spans another twelve thousand years or so, as humanity slowly rebuilds from catastrophe; as does the second part of the Sphidi saga, which runs contemporaneously but separately from it, rebuilding from the same catastrophe. Past the end of this period, the events of the two sagas reconnect.
						</li>
						<li>
							The third part of the Sphidi saga, with which no other stories are contemporary, spans a single lifetime again, as the virtual world reaches new heights; and unique among the periods of the Chronicles, this one begins to incorporate elements of first the Oumen saga and its setting of the rest of Earth, and then the Ehrban saga and its setting of all of Quelouva.
						</li>
					</ul>
					<p>There are multiple orders in which the stories can be read, some of them passing over parts of the story:</p>
					<ul>
						<li>
							One way is to read through the entire Ehrban saga, then the last series of the Oumen and Sphidi sagas to conclude that story, missing most of the Oumen and Sphidi sagas in the process.
						</li>
						<li>
							Another is to read the first series of the Ehrban and Oumen sagas as a prelude to reading the entire Sphidi saga through to conclusion, missing most of the Ehrban and Oumen sagas in the process.
						</li>
						<li>
							A third way is to read the first series of the Ehrban saga as a prelude to the Oumen saga, and then the last series of the Sphidi saga to conclude the story, missing most of the Ehrban and Sphidia sagas in the process.
						</li>
						<li>
							A fourth way is to read the first series of the Ehrban, Oumen, and Sphidi sagas, then the middle series of the Oumen saga, then the last series of the Ehrban, Oumen, and Sphidi sagas to conclude, missing only one series each out of the Ehrban and Sphidi sagas.
						</li>
						<li>
							A fifth way is to to read the first series of the Ehrban saga; then the first series of the Oumen saga and the second series of the Ehrban saga, which are contemporary; then the first series of the Sphidi saga, the second series of the Oumen saga, and the third series of the Ehrban saga, which are all contemporary; then the second series of the Sphidi saga and the third series of the Oumen saga, which are contemporary; and lastly the third series of the Sphidi saga, missing nothing at all.
						</li>
						<li>
							A sixth way is to read the entirety of the Ehrban saga in chronological order, skip back in time to read the Oumen saga in chronological order, then skip back again to read the Sphidi saga in chronological order, missing nothing at all.
						</li>
						<li>
							But the canonical reading begins in media res for each saga, and then explores in a spiraling fashion further and further into the past and future of each, unveiling the surprising interconnectedness of stories that at first do not appear related beyond the mysterious recurrence of a symbol common to all three sagas.
						</li>
					</ul>
