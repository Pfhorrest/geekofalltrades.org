	<h2>The Virtuality Series</h2>
	
	<ol>
		<li>
			In the Virtuality series,
			the only survivors of the Queen of Hell reconnect with Metis in the real world, accidentally giving the Queen access to Ehrban “magic”, and the whole galaxy barely survives.
			<ol>
				<li>
					In <a href="1">Virtuality Transcendent</a>,
					the reincarnations of Tom and Xiu who are all that survive of the ruined virtual afterlife awaken, through a series of intermediate virtual worlds, into robot bodies in the real world.
					<ol>
						<li>
							In <a href="1/1">Part 1</a>,
							Murdock and Annabelle are awakened from the nightmarish hellscape of Sphidia, recover their memories as Tom and Xiuying, and discover that they are still not in the real world.
						</li>
						<li>
							In <a href="1/2">Part 2</a>,
							Tom and Xiu are awakened into a dystopian Earth where they fight the automata oppressing humanity while recovering further memories that suggest that that world may still not be real.
						</li>
						<li>
							In <a href="1/3">Part 3</a>,
							Tom and Xiu struggle with the nature of reality itself before all is revealed to be a multi-layered deception helping them transition from Sphidia to the actual real world.
						</li>
					</ol>
				</li>
				<li>
					In <a href="2">Virtuality Incarnate</a>,
					other souls lost to the corrupt virtual afterlife are gradually recovered and reincarnated into increasingly advanced artificial bodies, culminating in ones powered by Ehrban “magic”.
					<ol>
						<li>
							In <a href="2/1">Part 1</a>,
							Tom and Xiu are reintroduced to the true Earth, struggle to accept that is is real after all they've been through, and begin to help others to transition from Sphidia to reality.
						</li>
						<li>
							In <a href="2/2">Part 2</a>,
							Xiuying takes the lead in helping the emerging Sphidian people, called Sphidi, integrate socially and politically with the organic humans of the real world.
						</li>
						<li>
							In <a href="2/3">Part 3</a>,
							Tom begins a program to grant greater morphological freedom to the Sphidi and to organic humanity along the way, culminating in bodies projected by Ehrban "magic" nanotechnology.
						</li>
					</ol>
				</li>
				<li>
					In <a href="3">Virtuality Immanent</a>,
					the mad Queen of Hell who destroyed the virtual afterlife from inside commandeers the “magic” bodies of the salvaged souls, and with their power nearly destroys the galaxy.
					<ol>
						<li>
							In <a href="3/1">Part 1</a>,
							the Queen of Hell "kills" the virtual forms of the Sphidi, transforming all of them but Tom and Xiu into powerful "Demons" whom the Ehrban "magic" technology continues to project into reality.
						</li>
						<li>
							In <a href="3/2">Part 2</a>,
							the magically projected Demons quickly overrun the entire galaxy despite the efforts of its so-called "gods" to stop them, prompting Metis to seek out Keius Meij for help.
						</li>
						<li>
							In <a href="3/3">Part 3</a>,
							Keius helps Metis defeat the Demons and the Queen of Hell, as well as Kron and the Four Horsemen, and the Berol, while revealing profound secrets of reality along the way.
						</li>
					</ol>
				</li>
			</ol>
		</li>
	</ol>

