	<h2>The Ehrban Saga</h2>
	
	<ol>
		<li>
			In the Ehrban saga,
			an apparently magical world turns out to be the fallen remnant of an ancient alien empire, which eventually rises again to reclaim its lost heritage.
			<ol>
				<li>
					In <a href="1">the Antiquity series</a>,
					Keius Meij discovers a technological “magic” and transforms Ehrban society with it until their galactic civilization collapses in the face of an alien threat.
					<ol>
						<li>
							In <a href="1/1">The Magic of Antiquity</a>,
							xenobiologist Keius Meij discovers a technological “magic”, saves his galactic civilization from civil war with it, and tracks down its extragalactic origins.
						</li>
						<li>
							In <a href="1/2">The Children of Antiquity</a>,
							Keius and his lover Tatia found a school of “mages”, create artificial life, conceive a natural child, and unexpectedly discover primitive humanity.
						</li>
						<li>
							In <a href="1/3">The War of Antiquity</a>,
							a plan by Keius to accelerate the study of humanity plus a failed plot by his secret adversaries accidentally creates an enemy that topples Ehrban civilization.
						</li>
					</ol>
				</li>
				<li>
					In <a href="2">the Meij series</a>,
					the descendants of Keius Meij help Ehrban civilization slowly rebuild on their ruined homeworld back to a spacefaring civilization again.
					<ol>
						<li>
							In <a href="2/1">Lievus Meij</a>,
							the child of Keius and Tatia, Lievus, and his half-sister, Kadia, become heads of rival factions of the “magical” survivors on the Ehrban homeworld.
						</li>
						<li>
							In <a href="2/2">Darak Meij</a>,
							the last living descendants of Lievus and Kadia face off in a battle that kills them both, drives “mages” into secrecy, and ushers in a new modernity for the surviving Ehrban.
						</li>
						<li>
							In <a href="2/3">Meij Endaren</a>,
							the secret society of “mages” oppose the technologically-advanced Nurbal's plans for space exploration, culminating in an exodus of the Nurbal and their allies to another world.
						</li>
					</ol>
				</li>
				<li>
					In <a href="3">the Nurbal series</a>,
					Amouch leads the Nurbal exodus back out into the galaxy to reclaim the scattered remains of the ancient Ehrban galactic civilization.
					<ol>
						<li>
							In <a href="3/1">Nurbal Colony</a>,
							the exiled Nurbal and their allies, lead by Meij-clone Amouch, establish themselves on their new home world with the risky use of taboo artificial intelligence.
						</li>
						<li>
							In <a href="3/2">Nurbal Legacy</a>,
							the Nurbal and their automatons reactivate the ancient Ehrban wormhole network and discover other survivors of the ancient war on other worlds.
						</li>
						<li>
							In <a href="3/3">Nurbal Enemy</a>,
							the automatons of the Nurbal mutually annihilate with the artificial life Keius created long ago, just before the Nurbal re-encounter their ancient enemy and the war is reignited.
						</li>
					</ol>
				</li>
			</ol>
		</li>
	</ol>

</section>