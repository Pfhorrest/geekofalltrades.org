	<h2>(7x03) Nurbal Colony: Part 1, Episode 3</h2>

	<p>
		As the de facto leader of this entire colony, Amouch must help establish social norms that will accommodate the various ways of life of the different peoples, and resist the Nurbal's inclination to force assimilation into their way of life &ndash; or further, into their semi-collective consciousness under the Machine. Though connected to the Machine himself, Amouch is particularly strong of mind because of his Meij heritage, and wields unusual influence over the Machine and through it the rest of the Nurbal &ndash; the very purpose for which they created him, in fact.
	</p>

	<p>
		Amouch separately approaches the leaders of each of the different peoples to official hear their grievances on behalf of the Nurbal. In doing so, he learns of a growing plan to awaken the entirety of the colonist population held in stasis on non-Nurbal ships, so as the deprive the Nurbal of numerical majority and sway influence away from those controlled by the Machine. This greatly concerns Amouch as their tiny fledgling colony cannot support such numbers of waking people in it so soon.
	</p>
	
	<p>
		Wielding his influence over the Machine and through it the Nurbal, and negotiating more traditionally with the leaders of the other peoples of Entu, Amouch convenes a constitutional convention to establish inalienable rights to protect the way of life of the minority peoples without requiring them to win those rights by numerical force. Chief among those rights is freedom of thought, in the sense of freedom from connection to any kind of central intelligence of hive mind &ndash; escaping that being one of the main reasons many colonists wanted to leave Niarba and not remain under the role of the Ancients. With such a political structure established, things begin to operate more fluidly in the young colony, and work begins in earnest cooperation between all parties toward developing it further.
	</p>
	
