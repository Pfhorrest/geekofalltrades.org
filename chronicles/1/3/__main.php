	<h2>The Nurbal Series</h2>
	
	<ol>
		<li>
			In the Nurbal series,
			Amouch leads the Nurbal exodus back out into the galaxy to reclaim the scattered remains of the ancient Ehrban galactic civilization.
			<ol>
				<li>
					In <a href="1">Nurbal Colony</a>,
					the exiled Nurbal and their allies, lead by Meij-clone Amouch, establish themselves on their new home world with the risky use of taboo artificial intelligence.
					<ol>
						<li>
							In <a href="1/1">Part 1</a>,
							the Nurbal and other exiles from Niarba arrive at the site of their first colony, where tensions between their different ways of life are eventually mediated by Amouch.
						</li>
						<li>
							In <a href="1/2">Part 2</a>,
							Amouch is elected first president of the new government, then quickly ousted and forced to find another role in life as he explores his magical heritage.
						</li>
						<li>
							In <a href="1/3">Part 3</a>,
							the colonists give full autonomy to their AI against Amouch's warnings, prompting him to go into stasis in case his magical power is needed against the AI in the future.
						</li>
					</ol>
				</li>
				<li>
					In <a href="2">Nurbal Legacy</a>,
					the Nurbal and their automatons reactivate the ancient Ehrban wormhole network and discover other survivors of the ancient war on other worlds.
					<ol>
						<li>
							In <a href="2/1">Part 1</a>,
							a century later, the colonists discover survivors on other worlds, and as Amouch is awakened from stasis he becomes worshipped by them as a religious icon.
						</li>
						<li>
							In <a href="2/2">Part 2</a>,
							Amouch must walk a fine line to find the proper stance to take in his new religious role in the burgeoning Quelovan Alliance of worlds.
						</li>
						<li>
							In <a href="2/3">Part 3</a>,
							still more worlds full of survivors oppose the Alliance on alternately religious or technological grounds, and Amouch works delicately to mediate those disputes.
						</li>
					</ol>
				</li>
				<li>
					In <a href="3">Nurbal Enemy</a>,
					the automatons of the Nurbal mutually annihilate with the artificial life Keius created long ago, just before the Nurbal re-encounter their ancient enemy and the war is reignited.
					<ol>
						<li>
							In <a href="3/1">Part 1</a>,
							the automatons of the Alliance encounter the surviving Asiron and instigate a war with them that quickly annihilates both sides while leaving the Ehrban unharmed.
						</li>
						<li>
							In <a href="3/2">Part 2</a>,
							the Quelouvan Alliance and the formerly Asiron-protected worlds must band together as the Berol reemerge into the galaxy and their ancient war is reignited.
						</li>
						<li>
							In <a href="3/3">Part 3</a>,
							the Ehrban rediscover humanity while searching for a weapon against the Berol, finding what they needed to win the war only to learn that that such power cuts both ways.
						</li>
					</ol>
				</li>
			</ol>
		</li>
	</ol>

