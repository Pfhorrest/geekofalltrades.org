							<h3>On Morality, Goodness, and Justice</h3>

							<p>
								With regards to opinions about <strong>morality</strong>, commensurablism boils down to forming initial opinions on the basis that something, loosely speaking, <strong>feels good (and not bad)</strong>, and then rejecting that and finding some other opinion to replace it with if someone should come across some circumstance wherein it <strong>feels bad</strong> in some way.
							</p>
							<p>
								And, if two contrary things both <strong>feel good or bad</strong> in different ways or to different people or under different circumstances, commensurablism means taking into account all the different ways that things <strong>feel</strong> to different people in different circumstances, and coming up with something new that <strong>feels good (and not bad)</strong> to everyone in every way in every circumstance, at least those that we've considered so far.
							</p>
							<p>
								In the limit, if we could consider absolutely every way that absolutely everything <strong>felt</strong> to absolutely everyone in absolutely every circumstance, whatever still <strong>felt good</strong> across all of that would be the universal <strong>good</strong>.
							</p>
							<p>
								In short, the universal <strong>good</strong> is the limit of what still seems <strong>good</strong> upon further and further investigation. We can't ever reach that limit, but that is the direction in which to improve our opinions about <strong>morality</strong>, toward more and more correct ones. Figuring out what what can still be said to <strong>feel good</strong> when more and more of that is accounted for may be increasingly difficult, but that is the task at hand if we care at all about the <strong>good</strong>.
							</p>
							<p>
								(It is trivially simple to satisfy everyone's different <strong>appetites</strong> with a <strong>strategy</strong> to put us all in different virtual worlds and feed us different experiences, but <strong>strategies</strong> that involve us all being in the same world together get trickier).
							</p>
							<p>
								This commensurablist approach to <strong>morality</strong> may be called <strong>"liberal hedonic moralism"</strong>, as <strong>moralism</strong> is the <strong>prescriptive</strong> face of universalism, <strong>hedonism</strong> is the <strong>prescriptive</strong> face of phenomenalism, and what I would call a critical-liberal methodology is more commonly called just <strong>"liberal"</strong> as applied to theories of <strong>justice</strong>.
							</p>
