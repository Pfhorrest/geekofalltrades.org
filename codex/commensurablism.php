							<li>
								"<strong>Objectivism</strong>" is what I name the broader category that encompasses both:
								<ul>
									<li>
										<strong>universalism</strong> (which I support), and
									</li>
									<li>
									<strong>transcendentalism</strong> (<a href="transcendentalism">which I oppose</a>).
									</li>
								</ul>
							</li>
							<li>
								"<strong>Subjectivism</strong>" is what I name the broader category that encompasses both:
								<ul>
									<li>
										<strong>phenomenalism</strong> (which I support), and
									</li>
									<li>
										<strong>relativism</strong> (<a href="relativism">which I oppose</a>).
									</li>
								</ul>
							</li>
							<li>
								"<strong>Fideism</strong>" is what I name the broader category that encompasses both:
								<ul>
									<li>
										<strong>liberalism</strong> (which I support), and
									</li>
									<li>
										<strong>dogmatism</strong> (<a href="dogmatism">which I oppose</a>).
									</li>
								</ul>
							</li>
							<li>
								"<strong>Skepticism</strong>" is what I name the broader category that encompasses both:
								<ul>
									<li>
										<strong>criticism</strong> (which I support), and
									</li>
									<li>
										<strong>cynicism</strong> (<a href="cynicism">which I oppose</a>).
									</li>
								</ul>
							</li>
						</ul>
						<p>
							(Henceforth I will use each of these terms in these specific senses only, to have clear labels for these positions. I am aware that many of these terms have uses other than the ones I am putting them to here, e.g. "objectivism" being used as the name of Ayn Rand's philosophy and "liberalism" being used in a variety of different political ways, and unless specifically mentioned I do not mean to imply any association with those usages where they differ from these ones).
						</p>
						<figure>
							<picture>
								<source srcset="images/commensurablism.svg" />
