								<img src="images/commensurablism.jpg"
									alt="Commensurablism" />
							</picture>
						</figure>
						<p>
							Phenomenalism, as anti-transcendentalism, is entailed by criticism, or anti-dogmatism: if we are going to hold every opinion open to question, we have to consider only opinions that would make some experiential, phenomenal difference, where we could somehow tell if they were correct or incorrect. (At least, unless we're willing to also reject universalism for relativism, and say that there are some questions about things beyond experience that simply can never be objectively answered. But as relativism itself also entails phenomenalism &ndash; for if there can be no apprehension of the objective then we have only subjective appearances to go on &ndash; we are still left with phenomenalism in any case.)
						</p>
						<p>
							And liberalism, as anti-cynicism, is entailed by universalism, or anti-relativism: if we are going to hold that such a thing as a correct opinion is possible, we have to give every opinion the benefit of the doubt that that one might possibly be it, otherwise we would be forced to dismiss all opinions as equally incorrect out of hand. (At least, unless we're willing to also reject criticism for dogmatism, and say that there are simply some foundational opinions that are beyond question. But as dogmatism itself also entails liberalism &ndash; for to take anything on faith we must suspend skeptical questioning &ndash; we are still left with liberalism in any case.)
						</p>
						<p>
							Because of this, "criticism" and "universalism" are enough to summarize my entire general philosophy, and it would suffice to name that general philosophy "critical universalism" or "universalist criticism".
						</p>
						<p>
							However I prefer a different, more succinct name: "commensurablism". For two things to be commensurable is for there to be some way to compare and measure them against each other, and I call my philosophy "commensurablism" because, all of the above taken into account together, its defining feature is that opinions of any sort can be compared and measured against each other to see which is more or less correct than the other.
						</p>
						<hr />
						<p>
							These core principles of commensurablism apply equally to both descriptive questions about reality, and prescriptive questions about morality, but some of them are more familiar under different names on different sides of that divide:
						</p>
						<ul>
							<li>
								Universalism about reality is usually just called <strong>"realism"</strong>, while universalism about morality might likewise be called <strong>"moralism"</strong>: one holds that some things are actually real, and the other that some things are actually moral, neither is just mere opinion.
							</li>
							<li>
								Similarly, phenomenalism about reality is usually called <strong>"empiricism"</strong>, while phenomenalism about morality is usually called <strong>"hedonism"</strong>: one appeals to sense experiences for descriptive justification, while the other appeals to appetitive experiences for prescriptive justification.
							</li>
							<li>
								And a combination of the principles of criticism and liberalism to investigations of descriptive questions is usually called a <strong>"critical" philosophy of knowledge</strong>, while that same combination of principles applied to investigations of prescriptive questions is usually called a <strong>"liberal" philosophy of justice</strong>.
							</li>
						</ul>
