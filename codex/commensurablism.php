					<section id="spiral">
						<h3>Roughly Spiral Progress</h3>
						<p>
							Thus all of inquiry, on either factual or normative matters, is an unending process of trying to filter out opinions that we have reasons to think are the wrong ones, and to come up with new ones that still might be the right ones. But no matter your current best answer to either question, there is always some degree of uncertainty: you might be right, but you might be wrong. All we can do is narrow in further and further on less and less wrong answers.
						</p>
						<p>
							In a way this is somewhat comparable to the "spiral-shaped" progress described by philosophers such as Johanne Fichte and Georg Hegel. Imagine an abstract space of possible answers, with the correct answers lying most likely somewhere around the middle of that space. Our investigations whittle away further and further at all opposite extremes, theses and their antitheses, and then again at the remaining extremes of the resulting syntheses, again and again, indefinitely. The center of the area remaining after each step will consequently wander around the original complete space of possibilities in a manner that gradually "spirals", roughly speaking, closer and closer to wherever the correct answer is in that space.
						</p>
						<p>
							Fichte and Hegel's "spiral-shaped progress" of theses, antitheses, and syntheses is, I think, a bit too much an idealization of this process, but it is at least in the right general direction relative to its predecessors, in a way that is itself an illustration of this very process:
						</p>
						<p>
							Eliminating first the extremes, the thesis and antithesis, of viewing worldviews either as constant and static, or as progressing linearly in a given direction, a first approximation at a synthesis could be the notion of circular change, alternating between opposites in a constant pattern. Hegel's notion of spiral progress is a further refinement upon that, a synthesis between linear progress and circular change, a view of alternating between opposites but narrowing in constantly toward some limit.
						</p>
						<p>
							My view is a refinement further still, which can perhaps be framed as the synthesis of Hegel's view, and the view that there is no pattern at all to change, just random or at least chaotic, unpredictable change. In my view the changes of worldview are largely unpredictable and unstructured, but by constantly weeding out the untenable extremes, the chaotic swinging between ever-less-extreme opposites still tends generally toward some limit over time.
						</p>
						<p>
							Commensurablism is itself explicitly such a synthesis of opposing views. As described already in the <a href="./#aspiration">introduction</a>, the history of philosophy is itself a series of diverging theses and antitheses punctuated by unifying syntheses, and I aim to position this philosophy as a synthesis of the contemporary pair of thesis and antithesis in that series, Analytic and Continental philosophy.
						</p>
						<p>
							It is furthermore a synthesis of two opposing trends in general public thought that I observe in my contemporary culture, that very loosely track affinity to those professional philosophical schools.
							One of them places utmost emphasis on the physical sciences and the elite academic authorities thereof, largely denying the universality of morality entirety. The other places their utmost emphasis on the ethical and political authority of the general populace, while largely denying the universality of reality entirely.
						</p>
						<p>
							But each of the faults of each of those trends of thought stem ultimately from haphazardly falling one way or another into one of the two worldviews that commensurablism is most truly a synthesis of: fideist objectivism and skeptical subjectivism. I aim to adapt and shore up the strengths of each of those opposing views, while rejecting those parts of each against which the other has sound arguments, resulting in this new view that retains the best of both and the worst of neither, being critical yet universalist about both reality and morality.
						</p>
					</section>

					<section id="opposition">
						<h3>The Opposition</h3>
						<p>
							Some philosophers, such as Ludwig Wittgenstein, end up advocating what is called a "quietism" about philosophy by the time they reach the end of their own philosophizing. A quietism about philosophy is the view that there is nothing positive to say on the topic of philosophy, only things to say about what <em>not</em> to do, and so once some philosopher has spoken against the errors that are commonly made by all other philosophers, there is nothing more to say about philosophy: after that, philosophy should not be done anymore, because to say anything else would only be to philosophize badly.
						</p>
						<p>
							I think that philosophy is no different than any other topic in that respect. Critical-liberal methods as I've already advocated above mean that all you're ever justified in saying on <em>any</em> topic is that the answer to a question is <em>not</em> within a certain range of possibilities, only ever saying that it <em>must be</em> in some range or another to the extent that that is just a rephrasing of saying what range it must not be within. In theology, this "negative way" is sometimes called "apophatic" &ndash; when theologians speak only of what God is <em>not</em> like, rather than saying anything about how they think he positively <em>is</em> &ndash; and that term is sometimes extended to label philosophical approaches like this as well.
						</p>
						<p>
							Yet as already elaborated above, it is always still a <em>range</em> of possibilities that remains, a range that can always be further narrowed, a single exact answer never conclusively pinned down. Consequently, as much can still be said on the topic of philosophy as can be said on any other topic, as the process of narrowing down the possible answers &ndash; elaborating on what answers are not possible, as an apophatic philosopher sets out to do &ndash; continues indefinitely, never being finished.
						</p>
						<p>
							So unlike a quietist philosopher, I have no illusions that my work here &ndash; even if entirely correct &ndash; should be the last word. And if this work outlives me, I encourage the people of the future to refine and expand upon it, as I have no doubt will be necessary.
						</p>
						<p>
							But like a quietist philosopher, I do think that philosophy done well is largely if not entirely apophatic, about explaining the ways that philosophy may be done poorly, and why those ways are poor. To that end, before I get into any of my more specific views on the various sub-topics of philosophy, I must elaborate further on the broad ways in which I think philosophy is commonly done wrongly.
						</p>

						<hr />

						<figure>
							<picture>
								<source srcset="images/principles-commensurablism.svg" />
								<source srcset="images/principles-commensurablism.png" />
								<img src="images/principles-commensurablism.jpg"
									alt="Principles and Counter-Principles of Commensurablism" />
							</picture>
						</figure>
						<p>
							My phenomenalism may superficially sound similar to relativism (both being kinds of subjectivism), inasmuch as there being nothing more to things than their experiential qualities sounds superficially similar to there being no actual things at all, only the appearance of them. Conversely, my universalism may sound like it could entail transcendentalism (both being kinds of objectivism), for the same reasons but in reverse.
						</p>
						<p>
							Likewise, my liberalism may superficially sound similar to dogmatism (both being kinds of fideism), inasmuch as not requiring justification to hold an opinion sounds superficially similar to leaving some opinions beyond question. Conversely, my criticism may sound like it could entail cynicism (both being kinds of skepticism), for the same reasons but in reverse.
						</p>
						<p>
							This conflation of similar but importantly distinct principles &ndash; objectivisms (transcendent or merely universal), subjectivisms (relativistic or merely phenomenal), skepticisms (cynical or merely critical), and fideisms (dogmatic or merely liberal) &ndash; leads many people, I suspect, to see the only available options as a transcendent dogmatic view, or else a cynical relativist view:
						</p>
						<figure>
							<picture>
								<source srcset="images/principles-phobosophies.svg" />
								<source srcset="images/principles-phobosophies.png" />
								<img src="images/principles-phobosophies.jpg"
									alt="Phobosophies" />
							</picture>
						</figure>
						<p>
							Those who reject dogmatism, as I do, and correctly adopt its negation, criticism, <em>but wrongly equate liberalism with dogmatism and thus criticism with cynicism</em>, then correctly see that cynicism entails relativism, and so conclude that the only alternative to dogmatism is relativism. If they likewise correctly see that rejecting dogmatism entails rejecting transcendentalism, <em>but wrongly equate universalism with transcendentalism and thus phenomenalism with relativism</em>, they will likewise conclude that the only alternative to dogmatism is relativism. In either case, from their correct rejection of dogmatism, they find themselves seemingly but wrongly compelled to adopt relativism.
						</p>
						<p>
							Those who reject relativism, as I do, and correctly adopt its negation, universalism, <em>but wrongly equate phenomenalism with relativism and thus universalism with transcendentalism</em>, then correctly see that transcendentalism entails dogmatism, and so conclude that the only alternative to relativism is dogmatism. If they likewise correctly see that rejecting relativism entails rejecting cynicism, <em>but wrongly equate criticism with cynicism and thus liberalism with dogmatism</em>, they will likewise conclude that the only alternative to relativism is dogmatism. In either case, from their correct rejection of relativism, they find themselves seemingly but wrongly compelled to adopt dogmatism.
						</p>
						<figure>
							<picture>
								<source srcset="images/principles-incommensurablism.svg" />
								<source srcset="images/principles-incommensurablism.png" />
								<img src="images/principles-incommensurablism.jpg"
									alt="Incommensurablism" />
							</picture>
						</figure>
						<p>
							Much worse still than either of those, though, is the nonsense that comes when both universalism <em>and</em> criticism are rejected together: saying that nothing is objectively true, like a relativist, but then, rather than skeptically rejecting all claims, instead taking that to make all claims immune to skepticism, like a dogmatist, because they couldn't be objectively wrong either. This makes for a relativism that gives free reign to all forms of dogmatism, and a dogmatism that cloaks itself in relativistic armor.
						</p>
						<p>
							This forms a kind of bizarro-commensurablism, in that it's also positioning itself between cynical relativism and transcendent dogmatism, but in a way that adopts the <em>worst</em> of each rather than the <em>best</em> of each, completely inverting all of the principles of commensurablism. But even those who support this position are not <em>completely</em> misguided. I think they're merely neglecting to differentiate between similar but importantly distinct positions, and so ignoring some possibilities that would accomplish the ends I suspect they have in mind without the pitfalls that they end up in.
						</p>
						<p>
							I suspect that, just like me, they start from a place of objectivism and skepticism, and then, just like me, they reach subjective and fideistic conclusions from there. But they take objectivism to mean transcendentalism, rather than merely universalism as I do; and they take skepticism to mean cynicism, rather than merely criticism as I do. That is to say, they take what is objective to mean whatever is <em>beyond any</em> mere appearances (rather than just <em>according with all</em> appearances, as I do), and they take being skeptical to mean rejecting everything we <em>don't</em> have solid reasons to <em>accept</em> (rather than just rejecting anything we <em>do</em> have solid reasons to <em>reject</em>, as I do).
						</p>
						<p>
							They therefore infer &ndash; validly, from the assumption of those incorrect premises &ndash; that there can be no apprehension of what is objective, and that all we have to go on is the subjective appearances of things, about which we are all equally free to think as we like, without having to answer to anyone's questioning. That conclusion <em>does</em> implicitly include within it my own principles of subjectivism-qua-phenomenalism and fideism-qua-liberalism; and I think that my endorsement of those principles <em>should</em> be enough to satisfy their concerns with my endorsement of objectivism-qua-universalism and skepticism-qua-criticism. But the transcendental and cynical premises they start from demand they take it further than that, into fideism-qua-dogmatism (as in, there are no grounds on which to ever call anybody's opinions wrong) and subjectivism-qua-relativism (as in, there are no grounds on which to ever suppose any opinion might be right).
						</p>
						<p>
							But as those principles we end up agreeing on, liberalism and phenomenalism, are the negations of their own cynical and transcendentalist premises, this entire line of reasoning ends up logically incoherent, unless they then reject the very premises they started from, and stick only with the now-unsupported conclusions. The conclusion of liberalism (entailed by their dogmatism) negates the premise of cynicism, which then undermines the argument for relativism, though that does not automatically disprove relativism. Likewise the conclusion of phenomenalism (entailed by that relativism) negates the premise of transcendentalism, which then undermines the argument for dogmatism, though that does not automatically disprove dogmatism.
						</p>
						<p>
							It is still logically possible to be a liberal phenomenalist, like we both are, without being a critical universalist, like I am and they are not; it is logically possible to be a liberal phenomenalist and also a dogmatic relativist, and all dogmatic relativists logically must also be liberal phenomenalists (though not vice versa). But then, as the premises of their arguments <em>against</em> criticism and univeralism, in favor of dogmatism and relativism, have been undermined by their own subsequent conclusions, we circle back around to the pragmatic reasons to assume criticism and universalism whenever we're unsure if they're correct or not, as laid out earlier in this essay: because to do otherwise is just to give up trying. And those principles together jointly entail the same phenomenalism and liberalism that they already endorse, without stalling out all hope of progress as either dogmatism or relativism would.
						</p>
						<p>
							That lack of hope of progress is the greatest threat of this last position. It treats everything as <em>meaningless</em>, because it allows no possibilities for anything to be either right or wrong, in either a descriptive or a prescriptive sense. And in doing so it shuts down all discourse about the merits, pro or con, of one opinion versus another, leaving everyone to merely have whatever opinions they happen to have and never sort out if there are any <em>reasons</em> to adopt one over the other.
						<p>
							This last position of dogmatic relativism bears many of the hallmarks of a type of view sometimes called "Postmodernism". But because that is, perhaps intentionally, a very vague and poorly-defined category, defined less by any of its own core principles and more in reference to a historical period in which the things it's opposed to were more common, I prefer instead to call it “incommensurablism”, and the supposed "Modernism" that it opposes "transcommensurablism". 
						</p>
						<p>
							For incommensurablism's defining tenet is precisely the opposite of commensurablism's: <em>no</em> opinions of any sort can be compared or measured against each other to see which is more or less correct than the other. But the position that its arguments actually target to reach that conclusion is not that of commensurablism, but instead the position beyond commensurablism at the opposite extremes, of transcendental cynicism, where the incommensurablists themselves started.
						</p>
						<hr />
						<p>
							As just detailed, I think that all of these positions I end up disagreeing with start from notions that I agree are generally in the right direction: some kind of objectivism or skepticism, which then respectively entail some kind of fideism and subjectivism. There are multiple ways of resolving the apparent conflicts that then arise. Some side with objectivism and fideism over skepticism and subjectivism; some side the opposite way; and some either just side with fideism and subjectivism over skepticism and objectivism, or else try to side with the latter so overzealously (for who wouldn't want absolute certainty about things beyond all appearances, if that were a coherent possibility) that they find themselves thereby forced into the former anyway. And in all those cases, at least some if not all of those reasonable starting notions end up discarded along the way.
						</p>
						<p>
							As in the Dogen quote in the <a href="/#intro">introduction</a>, before one studies philosophy, things make a naive kind of sense; then after an initial foray into philosophy, everything is turned on its head, into one form of nonsense or another. What I'm trying to build here in commensurablism is a way out of that nonsense, back into a more mature version of those reasonable starting positions, that can better withstand the temptation of such nonsense, and to show the common error underlying all of those different deviations from that initial core of common sense that we all start from.
						</p>
						<p>
							And what all of those responses share in common is the conflation of different types of objectivism (transcendent or merely universal), subjectivism (relativistic or merely phenomenal), skepticism (cynical or merely critical), and fideism (dogmatic or merely liberal) together. But they need not be so conflated.
						</p>
						<p>
							A <em>universalist</em> phenomenalism is not relativistic, and a <em>phenomenalist</em> universalism is not transcendent, viewing the world as wholly independent of anyone's opinions about it, but in no part inaccessible to all experience of it. Likewise a <em>critical</em> liberalism is not dogmatic, and a <em>liberal</em> criticism is not cynical, viewing the search for answers as an ongoing narrowing-down from initially unlimited possibilities, not the attempt to construct absolute certainties built upon foundations that themselves rest ultimately on nothing.
						</p>
						<p>
							The differentiation of those superficial similarities, and so the opening up of possibilities besides those extremes detailed above, is the key insight at the core of my entire general philosophy, embracing:
						</p>
						<ul>
							<li>
								universalism (the right objectivism) without transcendentalism (the wrong objectivism),
							</li>
							<li>
								criticism (the right skepticism) without cynicism (the wrong skepticism),
							</li>
							<li>
								liberalism (the right fideism) without dogmatism (the wrong fideism), and
							</li>
							<li>
								phenomenalism (the right subjectivism) without relativism (the wrong subjectivism).
							</li>
						</ul>
						<p>
							Over the next four essays, I will elaborate further on the implications of my core principles of universalism, criticism, liberalism, and phenomenalism, as they relate to various broad approaches to philosophy:
						</p>
						<ul>
							<li>
