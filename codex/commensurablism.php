				<section>

					<h2>The Philosophy of Commensurablism</h2>
				
					<p>
						In this essay I will lay out what my general philosophical views are, and my reasons for holding them, before I begin diving into the details of the implications of them on other broad approaches to philosophy, and on the many narrow subtopics of philosophy, in subsequent essays.
					</p>
					<p>
						This essay will begin with discussion of some extremely basic, hopefully uncontroversial, and potentially boring aspects of any kind of activity or discourse, but build from those boring basics up to a strong and likely controversial stance amidst various other philosophical possibilities. For as Bertrand Russell wrote, "The point of philosophy is to start with something so simple as not to seem worth stating, and to end with something so paradoxical that no one will believe it."
					</p>
					<p>
						I will first spend some time detailing exactly what position it is that I am going to argue for, in nuanced detail to make sure that there are no misunderstandings, before I then give an argument for it. Then I will explore the general implications of that position on different areas of philosophy in broad strokes, and finally compare and contrast that position to a number of others of comparable scope.
					</p>
					<hr />
					<p>
						Whenever we find ourselves trying to do anything at all, trying to make some state of affairs or another the case, there are always two questions it behooves us to ask:
					</p>
					<ul>
						<li>
							<em>how</em> to do it, how <em>does</em> that state of affairs come to be the case &ndash; a <em>descriptive</em> question; and 
						</li>
						<li>
							<em>why</em> to do it, why <em>should</em> that state of affairs come to be the case &ndash; a <em>prescriptive</em> question.
						</li>
					</ul>
					<p>
						But the answers to each of these kinds of questions can prompt more questions of the same kind in return. And as those chains of answers and further questions careen off toward infinity, we can begin to wonder how or even if there can ever be found ultimate answers to ground those chains of questions, in either direction. In a way, all of philosophy stems from wrestling with the discomforting prospect of these infinities, as daunting questions arise about both what if anything there really <em>is</em> at all, and <em>how</em> it came to be, and also what if anything there morally <em>ought to be</em> at all, and <em>why</em> it should be that way.
					</p>
					<p>
						At least initially, it seems that, regarding both of these matters:
					</p>
					<ul>
						<li>
							each of us is free to come to our own conclusions; and
						</li>
						<li>
							each of us has nothing to go off of but our own experiences.
						</li>
					</ul>
					<p>
						But whether that continues to be the case comes into question as soon as we communicate with, or even imagine the possibility of, other people who have different experiences or come to different conclusions. We are forced to confront the possibility that our opinions might be incorrect, and that there might be more to whatever is correct than just what we personally experience.
					</p>
					<p>
						At least for the most immediate questions of both of those types &ndash; how <em>did</em>, or why <em>should</em>, this in particular become the case, just in terms of an immediate cause or purpose &ndash; we tend to argue as though:
					</p>
					<ul>
						<li>
							some of our opinions are correct and others are incorrect, or at least some are more correct or incorrect than others; and
						</li>
						<li>
							we can sort out between us which of our opinions is which, or at least which lies more or less in one direction or the other.
						</li>
					</ul>
					<p>
						If we didn't think both of those things, then we wouldn't be arguing in the first place, as there would be no point if nobody was correct or incorrect at all, or if there weren't any question as to who it was.
					</p>
					<p>
						The core of my general philosophy, for which I will argue in a moment, is that those two basic premises implied in every normal argument should always be treated as correct, even as the chains of subsequent questions careen off to infinity and big philosophical issues rear their heads, because either "I'm just right and you're just wrong" (supposing that some answers are unquestionable) or "there’s no such thing as right or wrong" (supposing that some questions are unanswerable) are nothing but lazy ways to dodge the argument, avoiding the potential of having to change our opinions, and so cutting us off from all potential to learn and so improve our opinions.
					</p>
					<p>
						And I find that those two basic premises implied in every normal argument actually reinforce, rather than undermine, the starting assumptions that everyone is free to come to their own conclusion, and that there is nothing to go off of but experiences. For as I will elaborate momentarily, denying the former would end up tantamount to saying "there’s no such thing as right or wrong", and denying the latter would end up tantamount to saying "I'm just right and you're just wrong".
					</p>
					<p>
						So my general philosophy could be most succinctly summed up as the rejection of both unquestionable answers (answers that are not to be questioned), and unanswerable questions (questions that cannot be answered).
							By this I mean trusting that there are answers to be had, but always questioning every proposed answer; and entertaining the possibility of anything that might be an answer, but rejecting anything that's beyond questioning.
					</p>
					<section id="principles">
						<h3>Four Core Principles</h3>
						<p>
							In other words, I hold that, for all meaningful questions:
						</p>

						<ul>
							<li>
								There is such a thing as a correct opinion, in a sense beyond mere subjective agreement. (A position I call "universalism", and its negation "relativism".)
							</li>
							<li>
								There is always a question as to which opinion, and whether or to what extent any opinion, is correct. (A position I call "criticism", and its negation "dogmatism".)
							</li>
						</ul>
						<p>
							These in turn entail, for reasons I will elaborate momentarily, that:
						</p>
						<ul>
							<li>
								The initial state of inquiry is one of several opinions competing as equal candidates, none either winning or losing out by default, but each remaining a live possibility until it is shown to be worse than the others. (A position I call "liberalism", and its negation "cynicism").
							</li>
							<li>
								Such a contest of opinion is to be settled by comparing and measuring the candidates against the common scale of the experiential phenomena accessible by everyone, and opinions that cannot be thus tested are thereby disqualified. (A position I call "phenomenalism", and its negation "transcendentalism".)
							</li>
						</ul>

