							<h3>On Reality, Truth, and Knowledge</h3>

							<p>
								With regards to opinions about <strong>reality</strong>, commensurablism boils down to forming initial opinions on the basis that something, loosely speaking, <strong>looks true (and not false)</strong>, and then rejecting that and finding some other opinion to replace it with if someone should come across some circumstance wherein it <strong>looks false</strong> in some way.
							</p>
							<p>
								And, if two contrary things both <strong>look true or false</strong> in different ways or to different people or under different circumstances, commensurablism means taking into account all the different ways that things <strong>look</strong> to different people in different circumstances, and coming up with something new that <strong>looks true (and not false)</strong> to everyone in every way in every circumstance, at least those that we've considered so far.
							</p>
							<p>
								In the limit, if we could consider absolutely every way that absolutely everything <strong>looked</strong> to absolutely everyone in absolutely every circumstance, whatever still <strong>looked true</strong> across all of that would be the universal <strong>truth</strong>.
							</p>
							<p>
								In short, the universal <strong>truth</strong> is the limit of what still seems true upon further and further investigation. We can't ever reach that limit, but that is the direction in which to improve our opinions about <strong>reality</strong>, towards more and more correct ones. Figuring out what can still be said to <strong>look true</strong> when more and more of that is accounted for may be increasingly difficult, but that is the task at hand if we care at all about the <strong>truth</strong>.
							</p>
							<p>
								(It is trivially simple to satisfy everyone's different <strong>sensations</strong> with a <strong>theory</strong> that we’re all in different virtual worlds being fed different experiences, but <strong>theories</strong> that involve us all being in the same world together get trickier).
							</p>
							<p>
								This commensurablist approach to <strong>reality</strong> may be called <strong>"critical empirical realism"</strong>, as <strong>realism</strong> is the <strong>descriptive</strong> face of universalism, <strong>empiricism</strong> is the <strong>descriptive</strong> face of phenomenalism, and what I would call a critical-liberal methodology is more commonly called just <strong>"critical"</strong> as applied to theories of <strong>knowledge</strong>.
							</p>
						</section>

