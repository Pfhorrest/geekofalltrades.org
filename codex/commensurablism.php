					<section id="justification">
						<h3>Pragmatic Justification</h3>
						<p>
							The underlying reason I hold this general philosophical view, or rather my reason for rejecting the views opposite of it, is my <a href="metaphilosophy">metaphilosophy of analytic pragmatism</a>, taking a practical approach to philosophy and how best to accomplish the task it is aiming to do. To recap, that task is to find a way of discerning correct answers from incorrect answers to questions of any kind, whether about what is real or true or existent, or about what is moral or good or valuable.
						</p>
						<p>
							As commensurablism is just the conjunction of criticism and universalism, which are in turn just the negations of dogmatism and relativism, respectively, the core argument for it is thus to argue against both of those, on the pragmatic grounds that either of them completely undermines the possibility of doing that task of discerning correct answers from incorrect ones:
						</p>
						<ul>
							<li>
								If we accept dogmatism rather than criticism, then if our opinions should happen to be incorrect, we will never find out, because we never question them, and we will remain incorrect forever.
							</li>
							<li>
								If we accept relativism rather than universalism, then if there is such a thing as the correct opinion after all, we will never find it, because we never even attempt to answer what it might be, and we will remain incorrect forever.
							</li>
						</ul>
						<p>
							There <em>might not</em> be such a thing as as an objectively correct opinion, and even if there is, we <em>might not</em> be able to find it any more reliably than a shot in the dark of blind faith. But if we're starting from such a place of complete ignorance that we're not even sure about that &ndash; where we don't know what there is to know, or how to know it, or if we can know it at all, or if there is even anything at all to be known &ndash; and we <em>want</em> to figure out what the correct opinions are in case such a thing should turn out to be possible, then the safest bet, pragmatically speaking, is to proceed under the assumption that there are such things, and that we can find them, and then <em>try</em>. Maybe ultimately in vain, but that's better than definitely failing just because we never tried in the first place.
						</p>
						<hr />
						<p>
							To now summarize the argument for the entirety of commensurablism, that we have already worked backward through above:
						</p>
						<ul>
							<li>
								The task of philosophy is to find a way of discerning correct answers from incorrect answers to questions of any kind, whether about what is real or true or existent, or about what is moral or good or valuable.
							</li>
							<br />
							<li>
								The position that there is such a thing as a correct opinion, in a sense beyond mere subjective agreement, is to be called "universalism", and its negation "relativism".
							</li>
							<li>
								If we assume relativism rather than universalism, then in case there <em>does</em> happen to be such a thing as the correct opinion after all, we will never find it, because we never even attempt to answer what it might be, and we will remain incorrect forever.
							</li>
							<li>
								Therefore to successfully do philosophy at all we must at least tacitly assume universalism, rejecting <a href="relativism">relativism</a>.
							</li>
							<br />
							<li>
								The position that there is always a question as to which opinion, and whether or to what extent any opinion, is correct, is to be called "criticism", and its negation "dogmatism".
							</li>
							<li>
								If we assume dogmatism rather than criticism, then in case our opinions <em>do</em> happen to be incorrect after all, we will never find out, because we never question them, and we will remain incorrect forever.
							</li>
							<li>
								Therefore to successfully do philosophy at all we must at least tacitly assume criticism, rejecting <a href="dogmatism">dogmatism</a>.
							</li>
							<br />
							<li>
								The position that the initial state of inquiry is one of several opinions competing as equal candidates, none either winning or losing out by default, but each remaining a live possibility until it is shown to be worse than the others, is to be called "liberalism", and its negation "cynicism".
							</li>
							<li>
								Liberalism is entailed by universalism: if we are going to hold that such a thing as a correct opinion is possible, we have to give every opinion the benefit of the doubt that that one might possibly be it, otherwise we would be forced to dismiss all opinions as equally incorrect out of hand.
							</li>
							<li>
								Therefore, since to successfully do philosophy at all we must at least tacitly assume universalism, rejecting relativism, we must likewise at least tacitly assume liberalism, rejecting <a href="cynicism">cynicism</a>.
							</li>
							<br />
							<li>
								The position that any contest of opinion is to be settled by comparing and measuring the candidates against the common scale of the experiential phenomena accessible by everyone, and opinions that cannot be thus tested are thereby disqualified, is to be called "phenomenalism", and its negation "transcendentalism".
							</li>
							<li>
								Phenomenalism is entailed by criticism: if we are going to hold every opinion open to question, we have to consider only opinions that would make some experiential, phenomenal difference, where we could somehow tell if they were correct or incorrect.
							</li>
							<li>
								Therefore, since to successfully do philosophy at all we must at least tacitly assume criticism, rejecting dogmatism, we must likewise at least tacitly assume phenomenalism, rejecting <a href="transcendentalism">transcendentalism</a>.
							</li>
							<br />
							<li>
								Therefore to successfully do philosophy at all, to find any way of discerning correct answers from incorrect answers, we must at least tacitly assume universalism, criticism, liberalism, and phenomenalism; rejecting relativism, dogmatism, cynicism, and transcendentalism.
							</li>
						</ul>
						<hr />
						<p>
							This line of argument bears similarities to Blaise Pascal's "Wager", or pragmatic argument for believing in God. In the Wager, Pascal argues that if we cannot know whether or not God exists, we nevertheless cannot help but act on a tacit opinion one way or another, by either worshipping him or not. This results in four possible outcomes:
						</p>
						<ul>
							<li>
								either we believe in God, and he doesn't exist, and we lose a little in the wasted effort of worship;
							</li>
							<li>
								or we disbelieve in God, and he doesn't exist, and we save what little effort we would have spent in worship;
							</li>
							<li>
								or we believe in God, and he does exist, and we reap the infinite reward that is heaven;
							</li>
							<li>
								or we disbelieve in God, and he does exist, and we suffer the infinite loss that is hell.
							</li>
						</ul>
						<p>
							Pascal argues that it is thus the practically safest bet to believe in God, whether or not he turns out to actually exist. My pragmatic argument for commensurablism bears a formal similarity to that, in that I am likewise arguing that if we cannot know whether there are answers to our questions to be found, we nevertheless cannot help but act on a tacit opinion one way or another, by either trying to find them or not, resulting again in four possible categories of outcome:
						</p>
						<ul>
							<li>
								either we try to find the answers, and there are none that we could possibly find, and we lose the wasted effort of investigation;
							</li>
							<li>
								or we don't try to find the answers, and there are none that we could possibly find, and we save what effort we would have spent in investigation;
							</li>
							<li>
								or we try to find the answers, and there are some that we could find, and we reap the reward that is having them;
							</li>
							<li>
								or we don't try to find the answers, and there are some that we could find, and we suffer the loss that is never having them.
							</li>
						</ul>
						<p>
							Of course, exactly how much effort is wasted or saved depends on how much effort we would expend, and how much loss we suffer or reward we reap depends on what is at stake, and those factors change the exact odds of such a "wager". But as we can vary how much effort we expend at will, and there is always <em>something</em> at stake since everything we ever do depends upon the answers to the questions of how and why to do it, there is always <em>some</em> amount of effort for which the odds work out in favor of at least trying to figure things out, and it is never pragmatic to simply give up completely, as assuming either dogmatism or relativism would entail.
						</p>
						<p>
							The important key difference between Pascal's Wager and mine is that Pascal urges us to "bet" on one specific possibility, when there are many different possibilities with similar odds &ndash; different religions to choose from, different supposed Gods to worship and ways to worship them &ndash; leaving one forced to choose blindly which of those many options to bet on, and necessarily taking the worse option on all the other bets. Whereas I am only urging one to "bet" <em>at all</em>, to try something, anything, many different things, and at least see if any of them pan out, rather than just trying nothing and guaranteeing failure.
						</p>
						<p>
							To analogize the respective "wagers" to literal wagers on a horse race: Pascal is urging us to bet on a specific horse winning, rather than losing, while I am only urging us to bet on there being a bet at all, rather than not. If there is no bet, then we cannot lose the non-existent bet by betting in that non-existent bet that there will be a bet, even though we still might not win either, if there is indeed no bet to win.
						</p>
						<p>
							I would argue that to do otherwise than to try (even if ultimately in vain) to find answers to our questions, to fall prey to either relativism or dogmatism, to deny that there are such things as correct or incorrect opinions about either reality or morality, or to deny that we are able to figure out which is which, is actually not even philosophy at all.
						</p>
						<p>
							The Greek root of the word "philosophy" means "the love of wisdom", but I would argue that any approach substantially different from what I have laid out here as commensurablism would be better called "phobosophy", meaning "the fear of wisdom". For rather than seeking after wisdom, seeking after the ability to discern true from false or good from bad, it avoids it, by saying either that it is unobtainable, as the relativist does, or that it is unneeded, as the dogmatist does.
						</p>
						<p>
							Commensurablism could thus be said to be necessitated merely by being practical about the very task that defines philosophy itself. If you're trying to do philosophy at all, to pursue wisdom, the ability to sort out the true from the false and the good from the bad, you end up having to adopt commensurablism, or else just give up on the attempt completely, dismissing it as either hopeless or useless.
						</p>
						<p>
							As Henri Poincaré rightly said, <q>To doubt everything or to believe everything are two equally convenient solutions; both dispense with the necessity of reflection.</q> <!-- (La Science et l'Hypothèse, 1901). --> Or as Alfred Korzybski similarly said, <q>There are two ways to slide easily through life: to believe everything or to doubt everything; both ways save us from thinking.</q>
						</p>
					</section>
					
