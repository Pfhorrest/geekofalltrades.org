<section>
	<h2>Philosophy Study Guide</h2>
	<p classs="description"></p>
	<p>
		This document is a study guide for philosophy as a whole. It will offer two different orders in which the subject can be studied, but in both of them it will proceed breadth-first, giving students a broad but shallow overview of the whole subject to begin with, and then a deeper focus on narrower parts of the subject afterward.
	</p>

	<p>
		The main sources of study to be used will be three online encyclopedias. They are, in decreasing order of quality:
	</p>
	<ul>
		<li>
			<a href="https://plato.stanford.edu">The Stanford Encyclopedia of Philosophy</a> ("SEP")
		</li>
		<li>
			<a href="https://iep.utm.edu/">The Internet Encyclopedia of Philosophy</a> ("IEP")
		</li>
		<li>
			<a href="https://en.wikipedia.org/wiki/Philosophy">Wikipedia</a> ("WP")
		</li>
	</ul>

	<p>
		These sources will generally give their own sources in turn, the primary texts written by these philosophers about these topics, where students can then continue their philosophical study in further depth.
	</p>

	<p>The two different orders for studying philosophy that this guide will offer are:</p>

	<ul>
		<li>
			<b><a href="#historical">Historical order</a></b>, from ancient philosophy to modern philosophy, which is how most introductory classes on philosophy begin.
		</li>
		<li>
			<b><a href="#topical">Topical order</a></b>, from more theoretical philosophy to more applied philosophy, beginning with the logically prior technical topics like language, art, and mathematics, and building up from there to things like the relationship between science and religion, political philosophy, and the meaning of life.
		</li>
	</ul>

	<p>Both the historical and topical structure are borrowed from my own philosophy book, <i><a href="../">The Codex Quarentis</a></i>, but other than the emphasis placed on those historical figures and those topics of study, my own philosophical opinions about them are omitted here in lieu of the scholarly consensus from these encyclopedias.</p>

