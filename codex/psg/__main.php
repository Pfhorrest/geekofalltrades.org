	<section>
		<h2 id="topical">Topical order</h2>

		<figure class="invertible">
			<picture>
				<source srcset="../images/philosophy-structure.svg" />
				<source srcset="../images/philosophy-structure.png" />
				<img src="../images/philosophy-structure.jpg"
					alt="The structure of philosophy" />
			</picture>
		</figure>
		<p>
			The subtopics within philosophy can be arranged in relation to each other along a number of different axes, and two of the most important axes are the descriptive-to-prescriptive axis, and the theoretical-to-applied axis. These may seem superficially alike but they can be reckoned separately, orthogonally to each other. The descriptive topics have to do with things like existence, mind, knowledge, and education; while the prescriptive topics have to do with things like value, will, justice, and governance. Meanwhile, the theoretical topics have to do with things like the language with which we make claims about such things, either descriptive or prescriptive, and when and how such claims could be reckoned correct; while the applied topics have to do with how both those descriptive and prescriptive matters factor into how to live our lives in practice.
		</p>
		<ul>
			<li>
				<b>Philosophy of language</b>
				(
					<a href="https://iep.utm.edu/lang-phi/">IEP</a>
				|
					<a href="https://en.wikipedia.org/wiki/Philosophy_of_language">WP</a>
				)
				is perhaps the most theoretical topic within philosophy, being entirely about the meaning of words, in contrast to the meaning of life.
			</li>
			<br />
			<li>
				<b>Metaphysics</b>
				(
					<a href="https://plato.stanford.edu/entries/metaphysics/">SEP</a>
				|
					<a href="https://en.wikipedia.org/wiki/Metaphysics">WP</a>
				)
				is the broad and general descriptive topic, the philosophical inquiry into what is real.
			</li>
			<li>
				<b>Ethics</b>
				(
					<a href="https://iep.utm.edu/ethics/">IEP</a>
				|
					<a href="https://en.wikipedia.org/wiki/Ethics">WP</a>
				)
				is the broad and general prescriptive topic, the philosophical inquiry into what is moral.
			</li>
			<br />
			<li>
				<b>The meaning of life</b>
				(
					<a href="https://plato.stanford.edu/entries/life-meaning/">SEP</a>
				|
					<a href="https://en.wikipedia.org/wiki/Meaning_of_life">WP</a>
				)
				is the big applied topic that draws many people into philosophy to begin with, into which many if not all of the other philosophical topics factor.
			</li>
		</ul>
		<p>
			Near each of the poles of the theoretical-applied axis are other closely-related fields separated along the descriptive-prescriptive axis; and on each side of that descriptive-prescriptive axis proper, there are multiple fields separated not only along the theoretical-applied axis, but also along a third axis from <em>what</em> is real, moral, knowable, or just, to <em>who</em> are the subjects and institutions of reality, morality, knowledge, and justice.
		</p>
		<p>
			In this section of this study guide, we will proceed from the theoretical to the applied, on both the descriptive and prescriptive sides of things, and addressing both the <em>what</em> and the <em>who</em> questions as we go.
		</p>
		<ul>
			<li>
				<b>Logic</b>
				(
					<a href="https://en.wikipedia.org/wiki/Logic">WP</a>
				)
				is about the structure of and relationships between propositions, and so relates both to linguistic issues and mathematical issues.
			</li>
			<li>
				<b>Rhetoric</b>
				(
					<a href="https://en.wikipedia.org/wiki/Rhetoric">WP</a>
				)
				is about the style and presentation of speech acts, and so relates both to linguistic issues and aesthetic issues.
			</li>
			<br />
			<li>
				<b>Philosophy of mathematics</b>
				(
					<a href="https://plato.stanford.edu/entries/philosophy-mathematics/">SEP</a>
					|
					<a href="https://en.wikipedia.org/wiki/Philosophy_of_mathematics">WP</a>
				)
				is concerned with philosophical issues regarding mathematics, of course, and has significant overlap with both ontology and logic.
			</li>
			<li>
				<b>Aesthetics</b>
				(
					<a href="https://iep.utm.edu/aestheti/">IEP</a>
					|
					<a href="https://en.wikipedia.org/wiki/Aesthetics">WP</a>
				)
				is the topic concerned with beauty and philosophical issues regarding art, and has significant overlap with both axiology and rhetoric.
			</li>
			<br />
			<li>
				<b>Ontology</b>
				(
					<a href="https://en.wikipedia.org/wiki/Ontology">WP</a>
				)
				is the topic concerned with what exists.
			</li>
			<li>
				<b>Axiology</b>
				(
					<a href="https://plato.stanford.edu/entries/value-theory/">SEP</a>
					|
					<a href="https://en.wikipedia.org/wiki/Axiology">WP</a>
				)
				is the topic concerned with what is of value.
			</li>
			<br />
			<li>
				<b>Epistemology</b>
				(
					<a href="https://plato.stanford.edu/entries/epistemology/">SEP</a>
					|
					<a href="https://iep.utm.edu/epistemo/">IEP</a>
					|
					<a href="https://en.wikipedia.org/wiki/Epistemology">WP</a>
				)
				is the topic concerned with knowledge, or when and which thoughts or beliefs are justified or warranted, and why.
			</li>
			<li>
				<b>Justice</b>
				(
					<a href="https://plato.stanford.edu/entries/justice/">SEP</a>
					|
					<a href="https://iep.utm.edu/justwest/">IEP</a>
					|
					<a href="https://en.wikipedia.org/wiki/Justice">WP</a>
				)
				is about when and which acts or intentions are justified or warranted, and why.
			</li>
			<br />
			<li>
				<b>Philosophy of mind</b>
				(
					<a href="https://en.wikipedia.org/wiki/Philosophy_of_mind">WP</a>
				)
				is concerned with the nature of subjective experience, self-awareness, and the implications of that on what is or isn't real.
			</li>
			<li>
				<b>Free will</b>
				(
					<a href="https://plato.stanford.edu/entries/freewill/">SEP</a>
					|
					<a href="https://iep.utm.edu/freewill/">IEP</a>
					|
					<a href="https://en.wikipedia.org/wiki/Free_will">WP</a>
				)
				is about the agency underlying our behavior, self-control, and how that relates to moral responsibility.
			</li>
			<br />
			<li>
				<b>Philosophy of education</b>
				(
					<a href="https://plato.stanford.edu/entries/education-philosophy/">SEP</a>
					|
					<a href="https://en.wikipedia.org/wiki/Philosophy_of_education">WP</a>
				)
				is concerned with descriptive authority, about who gets to say what is the case or not. It is a major motive for concerns with enlightenment.
			</li>
			<li>
				<b>Political philosophy</b>
				(
					<a href="https://iep.utm.edu/polphil/">IEP</a>
					|
					<a href="https://en.wikipedia.org/wiki/Political_philosophy">WP</a>
				)
				is concerned with prescriptive authority, about who gets to say what ought to be the case or not. It is a major motive for concerns with empowerment.
			</li>
			<br />
			<li>
				<b>Enlightenment</b>
				(
					<a href="https://en.wikipedia.org/wiki/Enlightenment_(spiritual)">WP</a>
				)
				is a topic closely related to meaningfulness in life, but focusing more on the descriptive side, about inspiring the mind to actively pursue the truth, the real, the knowable, and so to become fully fully conscious or self-aware.
			</li>
			<li>
