					<h2>Summary</h2>
				
					<p>
						In the preceding essays I have laid out in detail all of my philosophical views. I will now go over them again more quickly in a high-level summary:
					</p>
					<hr />
					<p>
						Per my essay on <a href="action">action</a>, the possibility of success at any endeavor requires trying; but assuming either that success is impossible or that it is guaranteed means that there is no reason to try, and so renders success impossible. We must therefore always act according to a tacit assumption that success is both possible and not guaranteed.
					</p>
					<p>
						Per my pragmatist <a href="metaphilosophy">metaphilosophy</a>, all practical endeavors have at least a distant instrumental use for philosophy, in that the job of philosophy is to seek wisdom, meaning a way of discerning better from worse answers to questions about both reality and morality, giving the physical and ethical sciences the tools they need to do their jobs of enabling the creation of further tools and jobs with which to drive all manner of practical work.
					</p>
					<p>
						Doing philosophy successfully thus requires that we assume what I call <a href="commensurablism">commensurablism</a>, or universalist criticism, which is the position that, to questions about both reality and morality:
						<ul>
							<li>
								It is possible to achieve such wisdom, in other words it is possible to give genuinely correct answers to questions, a position that I term "universalism"; and
							</li>
							<li>
								Such wisdom is not guaranteed, in other words no proposed answer is ever definitively correct beyond all question, a position that I term "criticism".
							</li>
						</ul>
					</p>
					<hr />
					<p>
						Universalism, being the commitment to there being universal answers to all meaningful questions, is the negation of what I term "<a href="relativism">relativism</a>", which in turn encompasses:
					</p>
					<ul>
						<li>
							Solipsism and egotism
						</li>
						<li>
							Particular types of idealism
						</li>
					</ul>
					<p>
						Criticism, being the commitment to always being open to critically questioning anything, is the negation of what I term "<a href="dogmatism">dogmatism</a>", which in turn encompasses:
					</p>
					<ul>
						<li>
							Appeals to authority
						</li>
						<li>
							Appeals to popularity
						</li>
					</ul>
					<p>
						Universalism demands always allowing the initial possibility that some proposed answer might be the correct one rather than rejecting every proposal at the outset, a position I term "liberalism"; equivalently, rejecting relativism demands also rejecting what I term "<a href="cynicism">cynicism</a>", which in turn encompasses:
					</p>
					<ul>
						<li>
							Justificationism 
						</li>
						<li>
							Scientism and constructivism
						</li>
					</ul>
					<p>
						Criticism demands appealing always to experiential phenomena to judge proposed answers to questions without appeals to faith, a position I term "phenomenalism"; equivalently, rejecting dogmatism demands also rejecting what I term "<a href="transcendentalism">transcendentalism</a>", which in turn encompasses:
					</p>
					<ul>
						<li>
							Supernaturalism and "supernurturalism"
						</li>
						<li>
							Particular types of materialism
						</li>
					</ul>
					<hr />
					<p>
						All of that in turn demands a philosophy of <a href="language">language</a> that avoids:
					</p>
					<ul>
						<li>
							grounding the meaning of any claims in the attitudes of any particular privileged individual, so as to avoid dogmatism;
						</li>
						<li>
							reducing descriptive claims to prescriptive ones or vice versa, so as to avoid cynicism;
						</li>
						<li>
							grounding the truth of any claims in anything that is utterly beyond all experience, so as to avoid transcendentalism; or
						</li>
						<li>
							reducing any kind of claim to something that is not truth-apt at all, so as to avoid relativism.
						</li>
					</ul>
					<p>
						That philosophy of language in turn demands commensurate accommodation in the corresponding accounts of:
					</p>
					<ul>
						<li>
							<a href="logic">Logic and mathematics</a>, and 
						</li>
						<li>
							<a href="rhetoric">Rhetoric and the arts</a>
						</li>
					</ul>
					<hr />
					<p>
						Phenomenal universalism, which is to say the rejection of both transcendentalism and relativism, demands:
					</p>
					<ul>
						<li>
							An empirical realist <a href="ontology">ontology</a>
							<ul>
								<li>
									which demands a functionalist philosophy of <a href="mind">mind</a>,
								</li>
							</ul>
							and
						</li>
						<li>
							A hedonic moralist <a href="axiology">axiology</a> 
							<ul>
								<li>
									which demands a compatibilist philosophy of <a href="will">will</a>
								</li>
							</ul>
						</li>
					</ul>
					<hr />
					<p>
						Critical liberalism, the rejection of both dogmatism and cynicism, demands:
					</p>
					<ul>
						<li>
							A critical <a href="epistemology">epistemology</a>,
							<ul>
								<li>
									which demands a freethinking philosophy of <a href="education">education</a>,
									<ul>
										<li>
											which in turn depends upon an <a href="enlightenment">enlightened</a> academic community;
										</li>
									</ul>
								</li>
							</ul>
							and
						</li>
						<li>
							A liberal <a href="deontology">deontology</a>,
							<ul>
								<li>
									which demands an anarchic philosophy of <a href="governance">governance</a>,
									<ul>
										<li>
											which in turn depends upon an <a href="empowerment">empowered</a> political community.
										</li>
									</ul>
								</li>
							</ul>
						</li>
					</ul>
					<p>
						And cultivating and spreading such enlightenment and empowerment, and thus accessing and disseminating truths and goods, knowledge and justice, is both what makes the world meaningful to oneself, and makes oneself meaningful to the world, and is thus the <a href="action">meaning of life</a>.
					</p>
					<hr />
					<p>
						Having read through this high-level summary, I recommend reading this entire Codex through once more in detail with this bigger picture in mind, to better see how each smaller piece fits together into the unified whole that I hope you now understand.
					</p>

