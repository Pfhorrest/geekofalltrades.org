<section id="eternal">
	<h2>Eternal</h2>
	<p class="description">Beyond Infinity Lies Destiny.</p>
	<p>
		<a href="http://eternal.bungie.org/" rel="external">Eternal</a> is a free scenario for <a href="http://source.bungie.org" rel="external">Aleph One</a>,
		the multi-platform, open-source, first-person-shooter engine derived from <a href="http://www.bungie.com" rel="external">Bungie</a>'s <i>Marathon</i> engine.
		It continues the story of the <i>Marathon</i> trilogy, and is a nearly total conversion, featuring:
	</p>
	<ul>
		<li>Fifty-two new levels, some revisiting familiar locations from the original trilogy.</li>

		<li>Over six hundred fifty new high-resolution textures, including a dozen original
		landscape textures, from a variety of human and alien environments.</li>

		<li>Eight all new weapons, many of which must be scavenged from your defeated enemies.</li>

		<li>Over a dozen tracks of original music, including many remixes of your favorite tracks
		from the original Marathon.</li>

		<li>And several new creatures and characters, alongside the complete cast from the original
		trilogy.</li>
	</ul>
</section>

<section id="marathon">
	<h2>About Marathon</h2>
	<p class="description">Somewhere In The Heavens... They Are Waiting.</p>
	<p>
		<i>Marathon</i> was a landmark first-person shooter first released for the Macintosh in 1994,
		which introduced many new features and concepts to the genre including vertical aiming and mouselook;
		dual-wielded and dual-function weapons; versatile multiplayer
		modes such as King of the Hill, Kill the Man with the Ball, and cooperative play; friendly
		NPCs; and a deep and intricate narrative. The sequel, <i>Marathon 2: Durandal</i>, was released in
		1995, improving on the engine technologies and greatly expanding the scope of the series. In
		1996, <i>Marathon 2</i> was ported to Windows 95, and the <i>Marathon Infinity</i> package was released,
		including a new scenario using a modified <i>Marathon 2</i> engine, and most importantly, the tools
		used to build it, <i>Forge</i> and <i>Anvil</i>.
	</p>
	<p>
		In the year 2000, Bungie released the source code to the
		<i>Marathon 2</i> engine, and the <i>Marathon Open Source</i> project began, resulting in the new <i>Marathon</i>
		engine called <i>Aleph One</i>. Finally, in 2005, Bungie authorized free redistribution of the
		entire <i>Marathon</i> trilogy and all related files. This means that the entire trilogy can now be
		legally obtained for free and played on nearly any computer.
	</p>
</section>

